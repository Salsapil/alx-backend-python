#!/usr/bin/env python3
"""1. Basic annotations - concat"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings.

    Parameters:
    str1 (str): The first string.
    str2 (str): The second string.

    Returns:
    str: The concatenation of the two strings.
    """
    return (str1 + str2)
